module Timing
use Parallel
implicit none
save
private
public :: timer

!#######################################################################################################################!  
    contains


    subroutine timer(time)
        !$ use omp_lib
        real(8), intent(out) :: time

        if (omp) then
            !$ time = omp_get_wtime()
        else
            call cpu_time(time)
        endif
            
    end subroutine timer

end module Timing  
